def get_factor_color(factor: str) -> str:
    """获取因子对应的颜色"""
    return FACTOR_COLORS.get(factor, {}).get("hex", "#4A5D53")


def get_score_color(score: int) -> str:
    """根据分数获取对应颜色"""
    for (low, high), color_info in SCORE_COLORS.items():
        if low <= score <= high:
            return color_info["hex"]
    return "#999999"


def get_score_level(score: int) -> str:
    """根据分数获取等级名称"""
    for (low, high), color_info in SCORE_COLORS.items():
        if low <= score <= high:
            return color_info["level"]
    return "未知"


